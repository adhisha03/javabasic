package HW150;

public class Operators {
    public static void main(String[] args) {
        int x=29;
        int y=6;

//       Addition
        int a=x+y;
        System.out.println("Addition:"+a);
//     multiplication
         int b=x*y;
        System.out.println("multiplication:"+b);
//        subtract
         int c=x-y;
        System.out.println("subtract:"+c);
//        division
        int d=x/y;
        System.out.println("division:"+d);
//        reminder
        int e=x%y;
        System.out.println("reminder:"+e);
    }
}
